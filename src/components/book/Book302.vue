<script setup>
import { shallowRef } from 'vue';
const arr1 = shallowRef([10]); // 배열로 반응형 데이터 생성
const arr2 = shallowRef([10]); // 배열로 반응형 데이터 생성
setTimeout(() => {
  arr1.value = [10, 20]; // 배열의 값 자체를 변경
}, 3000);
setTimeout(() => {
  arr2.value[0] += 100; // 배열의 요소를 변경
  console.log(arr2.value[0])
}, 3000);

</script>
<template>
  <h3>302p-ref() 함수는 객체의 내용 변경은 감지 못함</h3>
  <h1>arr1: {{ arr1 }}</h1>
  <h1>arr2 : {{ arr2 }}</h1>
</template>
